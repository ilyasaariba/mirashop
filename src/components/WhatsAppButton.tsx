"use client";

import { useEffect, useState } from "react";
import { supabase } from "@/lib/supabase";

const FALLBACK_PHONE = "212768186124"; // always shows even if DB fetch fails

export default function WhatsAppButton() {
  const [phone, setPhone] = useState<string>(FALLBACK_PHONE);

  useEffect(() => {
    supabase
      .from("store_settings")
      .select("whatsapp_number")
      .limit(1)
      .single()
      .then(({ data }) => {
        if (data?.whatsapp_number) setPhone(data.whatsapp_number);
      });
  }, []);

  return (
    <a
      href={`https://wa.me/${phone}`}
      target="_blank"
      rel="noopener noreferrer"
      aria-label="تواصل معنا عبر واتساب"
      className="whatsapp-fab"
    >
      {/* WhatsApp SVG icon */}
      <svg
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 48 48"
        width="28"
        height="28"
        fill="white"
        aria-hidden="true"
      >
        <path d="M4.868 43.303l2.694-9.835a18.953 18.953 0 01-2.56-9.52C5.006 13.012 13.839 4.17 24.765 4.17c5.299.002 10.277 2.066 14.022 5.817a19.742 19.742 0 015.803 14.043c-.004 10.935-8.838 19.778-19.762 19.778h-.008a19.774 19.774 0 01-9.455-2.407l-10.497 2.902zm10.87-6.31l.602.357a16.417 16.417 0 008.414 2.314h.007c9.072 0 16.452-7.387 16.456-16.468a16.533 16.533 0 00-4.852-11.767 16.533 16.533 0 00-11.607-4.837c-9.079 0-16.459 7.385-16.463 16.464a16.449 16.449 0 002.557 8.775l.398.632-1.689 6.166 6.177-1.636zm14.62-9.051c-.128-.213-.47-.341-.983-.597-.512-.256-3.033-1.498-3.503-1.669-.47-.17-.811-.257-1.152.256-.342.512-1.323 1.669-1.621 2.011-.298.341-.597.384-1.11.128-.512-.256-2.164-.798-4.123-2.543-1.524-1.358-2.552-3.034-2.85-3.546-.298-.513-.032-.79.224-1.044.23-.23.512-.598.768-.896.256-.298.341-.512.512-.854.17-.341.086-.64-.043-.896-.129-.256-1.152-2.78-1.579-3.803-.413-.997-.835-.862-1.152-.878-.298-.014-.64-.017-.982-.017-.341 0-.896.128-1.366.64-.47.512-1.793 1.754-1.793 4.277s1.836 4.96 2.092 5.302c.256.341 3.61 5.51 8.75 7.729 1.222.528 2.175.843 2.918 1.079 1.226.39 2.342.335 3.224.203 1.086-.164 3.033-.897 3.46-1.963.428-1.067.428-1.98.299-2.171z" />
      </svg>
      <span className="whatsapp-fab-pulse" />
    </a>
  );
}
